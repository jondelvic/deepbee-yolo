def draw_boxes_on_image(image_path: Path, annotations: list[dict], out_path: Path):
    """Draw square bounding boxes (point + radius) on the full image and save."""
    img = cv2.imread(str(image_path))
    if img is None:
        print(f"  ⚠ Could not read {image_path}")
        return

    h, w = img.shape[:2]
    for ann in annotations:
        x, y, r, cls = ann["x"], ann["y"], ann["r"], ann["class_id"]
        # Convert to pixel coordinates (square box around centre)
        x1 = max(0, int(x - r))
        y1 = max(0, int(y - r))
        x2 = min(w - 1, int(x + r))
        y2 = min(h - 1, int(y + r))
        color = CLASS_COLORS_BGR[cls % len(CLASS_COLORS_BGR)]
        cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
        cv2.putText(img, CLASS_NAMES[cls], (x1, max(y1 - 4, 10)),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.5, color, 1, cv2.LINE_AA)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    cv2.imwrite(str(out_path), img, [cv2.IMWRITE_JPEG_QUALITY, 90])
    print(f"  🖼  Preview saved → {out_path}")


def main():
    # 1. Parse test CSV
    print("Parsing exhaustive annotations (test.csv)...")
    annotations = parse_test_csv(TEST_CSV)   # returns {image_name: [ann_dict, ...]}

    # Check for dontcare (the parser already prints a warning, but we'll collect)
    # We can't get the raw labels after parsing, but we can re-read CSV to find "dontcare".
    # For simplicity, we trust the parser's output – unknown classes are silently dropped.
    # But we can scan the CSV manually for "dontcare" to flag images.
    raw_dontcare = defaultdict(int)
    import pandas as pd
    df = pd.read_csv(TEST_CSV, dtype=str)
    df.columns = [c.strip().lower().replace(" ", "_") for c in df.columns]
    name_col = next(c for c in df.columns if "name" in c and "image" not in c)
    img_col  = next(c for c in df.columns if "image" in c)
    for _, row in df.iterrows():
        cls_raw = str(row[name_col]).strip().lower()
        if cls_raw == "dontcare":
            img_name = row[img_col].strip()
            raw_dontcare[img_name] += 1

    # 2. Prepare image lists
    all_images = sorted(annotations.keys())
    eval_images = [name for name in all_images if name in EVAL_IMAGE_NAMES]
    non_eval = [name for name in all_images if name not in EVAL_IMAGE_NAMES]

    print(f"\nTotal exhaustive images: {len(all_images)}")
    print(f"Pinned for eval: {len(eval_images)}  ({', '.join(eval_images) if eval_images else 'NONE'})")
    if len(eval_images) < len(EVAL_IMAGE_NAMES):
        missing = EVAL_IMAGE_NAMES - set(eval_images)
        print(f"⚠ Missing eval images (not found in test.csv): {missing}")

    # 3. Per-image annotation counts
    print("\n─── Per‑image class breakdown (all exhaustive) ───")
    header = f"{'Image':<40} {'Total':>6}   " + "   ".join(f"{n:>6}" for n in CLASS_NAMES)
    print(header)
    print("-" * len(header))

    total_counts = Counter()
    eval_counts = Counter()
    for img in sorted(all_images, key=lambda x: sum(len(a) for a in [annotations[x]]) if x in annotations else 0, reverse=True):
        anns = annotations.get(img, [])
        counts = Counter()
        for a in anns:
            counts[a["class_id"]] += 1
        total = sum(counts.values())
        total_counts.update(counts)
        if img in EVAL_IMAGE_NAMES:
            eval_counts.update(counts)

        class_str = "   ".join(f"{counts.get(i,0):>6}" for i in range(len(CLASS_NAMES)))
        tag = " <-- EVAL" if img in EVAL_IMAGE_NAMES else ""
        print(f"{img:<40} {total:>6}   {class_str}{tag}")

        if img in raw_dontcare:
            print(f"    ⚠ Contains {raw_dontcare[img]} 'dontcare' annotation(s) – ignored by pipeline.")

    # 4. Summary of class balance
    print("\n─── Class balance (all exhaustive vs eval subset) ───")
    print(f"{'Class':<10} {'All exhaustive':>15} {'Eval subset':>12} {'Eval % of total':>15}")
    print("-" * 55)
    for i, name in enumerate(CLASS_NAMES):
        all_c = total_counts.get(i, 0)
        eval_c = eval_counts.get(i, 0)
        pct = f"{100*eval_c/all_c:.1f}%" if all_c > 0 else "N/A"
        print(f"{name:<10} {all_c:>15} {eval_c:>12} {pct:>15}")

    # 5. Check imbalance specifically in eval
    if eval_counts:
        max_cls = max(eval_counts.values())
        min_cls = min(c for c in eval_counts.values() if c > 0)
        ratio = max_cls / min_cls if min_cls > 0 else float('inf')
        print(f"\nEval class imbalance ratio: {ratio:.1f}×")
        if ratio > 5:
            print("   ⚠ High imbalance detected. Consider adding more diverse eval images.")

    # 6. (Optional) Save visual previews of the two eval images
    if eval_images:
        print("\n─── Visualising eval images (bounding boxes drawn) ───")
        out_dir = Path("eval_image_check")
        for img_name in eval_images:
            img_path = RAW_IMAGES_TEST / img_name
            if not img_path.exists():
                # try extensions
                stem = Path(img_name).stem
                found = next(
                    (RAW_IMAGES_TEST / (stem + ext) for ext in [".jpg", ".jpeg", ".JPG", ".JPEG", ".png", ".PNG"]
                     if (RAW_IMAGES_TEST / (stem + ext)).exists()),
                    None
                )
                if found:
                    img_path = found
                else:
                    print(f"  ✗ Image not found: {img_name}")
                    continue
            draw_boxes_on_image(img_path, annotations[img_name], out_dir / f"{Path(img_name).stem}_boxes.jpg")
    else:
        print("\n(No eval images to visualise; check EVAL_IMAGE_NAMES in config.py)")

    # 7. Recommendation hint
    print("\n─── Recommendation ───")
    if len(eval_images) < 3:
        print("  • Eval set is very small (2 images). A single image bias can dominate metrics.")
        print("  • Consider using 3–4 images for more stable evaluation.")
    if raw_dontcare:
        print("  • 'dontcare' labels found in some images. They are currently ignored.")
        print("    Decide if you want to keep them as a separate 'ignore' class or accept the loss.")
    print("  • If class imbalance in eval is >5×, the benchmark may not fairly represent all classes.")
    print("    You could repick eval images to get a more balanced subset, or increase the number of eval images.")

if __name__ == "__main__":
    main()